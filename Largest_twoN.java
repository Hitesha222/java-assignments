//Assignment: 3 Find the largest number among two(if-else)

public class Largest_twoN {

	public static void main(String[] args) {

		int a = 23, b = 45;

		if (a > b) {
			System.out.println(a+" is largest number among " +a+" and " +b);
		}

		else {
			System.out.println(b+" is largest number among " +a+" and " +b);
		}
	}

}
